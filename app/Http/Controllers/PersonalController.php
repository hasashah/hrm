     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data=$request->all();

        $fileName="fileName".time().'.'.request()->picture->getClientOriginalExtension();

        $path = $request->picture->storeAs('picture',$fileName);

        $data['picture']=$path;

        Personal::create($data);

        return redirect()->route('personals.index')->withStatus('created Successfully');


    }

